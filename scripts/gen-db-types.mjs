/**
 * Writes Supabase-generated TypeScript to src/lib/database.generated.ts (UTF-8).
 * Strips CLI noise before the first `export type Json` line.
 *
 * Usage:
 *   node scripts/gen-db-types.mjs           # uses --linked (remote project from supabase link)
 *   node scripts/gen-db-types.mjs --local   # uses local `supabase start` DB
 */
import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const outPath = join(root, 'src', 'lib', 'database.generated.ts');

const useLocal = process.argv.includes('--local');
const sub = useLocal ? '--local' : '--linked';
const cmd = `npx supabase gen types typescript ${sub}`;

let stdout;
try {
  stdout = execSync(cmd, {
    cwd: root,
    encoding: 'utf8',
    maxBuffer: 20 * 1024 * 1024,
    stdio: ['ignore', 'pipe', 'inherit'],
  });
} catch (e) {
  console.error(e.stderr?.toString?.() || e.message);
  process.exit(e.status ?? 1);
}

const marker = 'export type Json';
const idx = stdout.indexOf(marker);
const body = idx >= 0 ? stdout.slice(idx) : stdout;

const banner = `/* eslint-disable */
/* Auto-generated by scripts/gen-db-types.mjs — do not edit by hand. Re-run: npm run gen:db-types */

`;

writeFileSync(outPath, banner + body, 'utf8');
console.log('Wrote', outPath);
